ll rangesum(ll a, ll b)
{
    ll sum1 = (b % MOD * ((b + 1) % MOD)) % MOD;
    sum1 = (sum1 * INV_2) % MOD;

    ll sum2 = (((a - 1) % MOD + MOD) % MOD * (a % MOD)) % MOD;
    sum2 = (sum2 * INV_2) % MOD;

    return (sum1 - sum2 + MOD) % MOD;
}

